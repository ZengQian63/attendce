package com.aboluo.attend.pojo;

public class Attendance {

    private int emp_id;
    private String emp_name;
    private String dept;
    private int late_num;
    private int leave_early_num;
    private int attend_day;
    private int absent_day;
    private int ask_leave_day;

    public int getEmp_id() {
        return emp_id;
    }

    public void setEmp_id(int emp_id) {
        this.emp_id = emp_id;
    }

    public String getEmp_name() {
        return emp_name;
    }

    public void setEmp_name(String emp_name) {
        this.emp_name = emp_name;
    }

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

    public int getLate_num() {
        return late_num;
    }

    public void setLate_num(int late_num) {
        this.late_num = late_num;
    }

    public int getLeave_early_num() {
        return leave_early_num;
    }

    public void setLeave_early_num(int leave_early_num) {
        this.leave_early_num = leave_early_num;
    }

    public int getAttend_day() {
        return attend_day;
    }

    public void setAttend_day(int attend_day) {
        this.attend_day = attend_day;
    }

    public int getAbsent_day() {
        return absent_day;
    }

    public void setAbsent_day(int absent_day) {
        this.absent_day = absent_day;
    }

    public int getAsk_leave_day() {
        return ask_leave_day;
    }

    public void setAsk_leave_day(int ask_leave_day) {
        this.ask_leave_day = ask_leave_day;
    }

    @Override
    public String toString() {
        return "Attendance{" +
                "emp_id='" + emp_id + '\'' +
                ", emp_name='" + emp_name + '\'' +
                ", dept='" + dept + '\'' +
                ", late_num=" + late_num +
                ", leave_early_num=" + leave_early_num +
                ", attend_day=" + attend_day +
                ", absent_day=" + absent_day +
                ", ask_leave_day=" + ask_leave_day +
                '}';
    }
}
