package com.aboluo.attend.pojo;

public class Charts {

    private int woman;
    private int man;
    private int zn_dept;
    private int dsj_dept;
    private int disable;
    private int total;

    public int getWoman() {
        return woman;
    }

    public void setWoman(int woman) {
        this.woman = woman;
    }

    public int getMan() {
        return man;
    }

    public void setMan(int man) {
        this.man = man;
    }

    public int getZn_dept() {
        return zn_dept;
    }

    public void setZn_dept(int zn_dept) {
        this.zn_dept = zn_dept;
    }

    public int getDsj_dept() {
        return dsj_dept;
    }

    public void setDsj_dept(int dsj_dept) {
        this.dsj_dept = dsj_dept;
    }

    public int getDisable() {
        return disable;
    }

    public void setDisable(int disable) {
        this.disable = disable;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    @Override
    public String toString() {
        return "Charts{" +
                "woman=" + woman +
                ", man=" + man +
                ", zn_dept=" + zn_dept +
                ", dsj_dept=" + dsj_dept +
                ", disable=" + disable +
                ", total=" + total +
                '}';
    }
}
